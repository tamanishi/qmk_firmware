static bool adjust_pressed = false;
static bool ctrl_pressed = false;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    // set_timelog();
  }

  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case LADJUST:
      if (record->event.pressed) {
        adjust_pressed = true;
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
        if (adjust_pressed) {
          register_code(KC_LBRC);
          unregister_code(KC_LBRC);
        }
        adjust_pressed = false;
      }
      return false;
      break;
    case RADJUST:
      if (record->event.pressed) {
        adjust_pressed = true;
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
        if (adjust_pressed) {
          register_code(KC_RBRC);
          unregister_code(KC_RBRC);
        }
        adjust_pressed = false;
      }
      return false;
      break;
    // implements CTL + H -> BS inside firmware
    case KC_LCTL:
      if (record->event.pressed) {
        ctrl_pressed = true;
      } else {
        ctrl_pressed = false;
      }
      break;
    case KC_H:
      if (ctrl_pressed) {
        register_code(KC_BSPC);
        unregister_code(KC_BSPC);
        return false;
      }
      break;
    default:
      if (record->event.pressed) {
        adjust_pressed = false;
      }
      break;
  }
  return true;
}
